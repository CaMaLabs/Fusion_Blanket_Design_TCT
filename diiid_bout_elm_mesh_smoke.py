def run_case(executable: Path, case_dir: Path) -> None:
    environment = dict(os.environ)
    environment["UCX_TLS"] = environment.get("BOUT_UCX_TLS", "self")
    result = subprocess.run(
        [str(executable), "-d", str(case_dir)],
        env=environment,
        capture_output=True,
        text=True,
        check=False,
    )
    (case_dir / "bout_stdout.log").write_text(result.stdout, encoding="utf-8")
    (case_dir / "bout_stderr.log").write_text(result.stderr, encoding="utf-8")
    if result.returncode:
        raise RuntimeError(f"BOUT++ failed for {case_dir.name}; see bout_stdout.log")


def summarize_case(case_dir: Path, mz: int) -> dict[str, Any]:
    output = case_dir / "BOUT.dmp.0.nc"
    if not output.exists():
        raise FileNotFoundError(output)
    result: dict[str, Any] = {"case": case_dir.name, "mz": mz, "fields": {}}
    with netCDF4.Dataset(output) as dataset:
        times = np.asarray(dataset.variables["t_array"][:], dtype=float)
        result["times"] = times.tolist()
        for name in OUTPUT_FIELDS:
            values = np.asarray(dataset.variables[name][:], dtype=float)
            physical = values[:, 2:-2, 2:-2, :]
            finite = np.isfinite(physical)
            result["fields"][name] = {
                "shape": list(values.shape),
                "physical_shape": list(physical.shape),
                "physical_domain_finite": bool(finite.all()),
                "guard_cells_contain_nonfinite": bool(
                    not np.isfinite(values[:, :2, :, :]).all()
                    or not np.isfinite(values[:, -2:, :, :]).all()
                    or not np.isfinite(values[:, :, :2, :]).all()
                    or not np.isfinite(values[:, :, -2:, :]).all()
                ),
                "initial_max_abs": float(np.max(np.abs(physical[0]))),
                "final_max_abs": float(np.max(np.abs(physical[-1]))),
                "final_l2": float(np.sqrt(np.mean(physical[-1] ** 2))),
            }
    result["all_physical_fields_finite"] = all(
        field["physical_domain_finite"] for field in result["fields"].values()
    )
    for path in case_dir.glob("BOUT.dmp.*.nc"):
        path.unlink()
    for path in case_dir.glob("BOUT.restart.*.nc"):
        path.unlink()
    settings = case_dir / "BOUT.settings"
    if settings.exists():
        settings.unlink()
    for path in case_dir.glob(".BOUT.pid.*"):
        path.unlink()
    for path in case_dir.glob("BOUT.log.*"):
        path.unlink()
    for name in ("bout_stdout.log", "bout_stderr.log"):
        path = case_dir / name
        if path.exists():
            path.unlink()
    return result


