def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def write_serial_topology_grid(source: Path, output: Path) -> dict[str, int]:
    output.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(source, output)
    with netCDF4.Dataset(output, "r+") as dataset:
        nx = len(dataset.dimensions["x"])
        ny = len(dataset.dimensions["y"])
        topology = {
            "ixseps1": nx,
            "ixseps2": nx,
            "jyseps1_1": -1,
            "jyseps1_2": ny // 2,
            "jyseps2_1": ny // 2,
            "jyseps2_2": ny - 1,
            "ny_inner": ny // 2,
        }
        for name, value in topology.items():
            dataset.variables[name].assignValue(value)
        shift_angle = np.asarray(dataset.variables["ShiftAngle"][:])
        shift_angle[~np.isfinite(shift_angle)] = 0.0
        dataset.variables["ShiftAngle"][:] = shift_angle
        dataset.setncattr(
            "validation_topology_override",
            "Serial smoke only: X-point connections disabled and undefined ShiftAngle values set to zero; geometry, magnetic, pressure, curvature, Jacobian, and metric fields unchanged.",
        )
    return topology


