def run_case(executable: Path, case_dir: Path) -> None:
    env = dict(os.environ)
    env["UCX_TLS"] = env.get("BOUT_UCX_TLS", "self")
    result = subprocess.run(
        [str(executable), "-d", str(case_dir)], env=env, capture_output=True, text=True, check=False
    )
    if result.returncode:
        (case_dir / "bout_stdout.log").write_text(result.stdout, encoding="utf-8")
        (case_dir / "bout_stderr.log").write_text(result.stderr, encoding="utf-8")
        raise RuntimeError(f"elm-pb failed for {case_dir.name}")


def summarize(case_dir: Path, mesh_name: str, mz: int, dt: float) -> dict[str, Any]:
    with netCDF4.Dataset(case_dir / "BOUT.dmp.0.nc") as dataset:
        times = np.asarray(dataset["t_array"][:], dtype=float)
        fields = {}
        for name in FIELDS:
            values = np.asarray(dataset[name][:], dtype=float)[:, 2:-2, 2:-2, :]
            l2 = np.sqrt(np.mean(values * values, axis=(1, 2, 3)))
            maximum = np.max(np.abs(values), axis=(1, 2, 3))
            fields[name] = {
                "finite": bool(np.isfinite(values).all()),
                "l2_history": l2.tolist(),
                "max_abs_history": maximum.tolist(),
                "final_l2": float(l2[-1]),
                "final_max_abs": float(maximum[-1]),
            }
    for pattern in ("BOUT.dmp.*.nc", "BOUT.restart.*.nc", "BOUT.log.*", ".BOUT.pid.*"):
        for path in case_dir.glob(pattern):
            path.unlink()
    for name in ("BOUT.settings",):
        path = case_dir / name
        if path.exists():
            path.unlink()
    return {"mesh": mesh_name, "mz": mz, "dt": dt, "times": times.tolist(), "fields": fields}


def relative_difference(a: float, b: float) -> float:
    return abs(a - b) / max(abs(a), abs(b), 1e-300)


def write_report(run_dir: Path, summary: dict[str, Any]) -> None:
    lines = [
        "# DIII-D BOUT++ elm-pb Serial-Topology Solution Convergence",
        "",
        f"- Status: `{summary['status']}`",
        f"- Generated: `{summary['generated_at']}`",
        "",
        "| Case | Mesh | MZ | dt | Final U L2 | Final P L2 | Final Psi L2 |",
        "| --- | --- | ---: | ---: | ---: | ---: | ---: |",
    ]
    for name, case in summary["cases"].items():
        lines.append(
            f"| `{name}` | {case['mesh']} | {case['mz']} | {case['dt']:.1e} | "
            f"{case['fields']['U']['final_l2']:.6e} | {case['fields']['P']['final_l2']:.6e} | "
            f"{case['fields']['Psi']['final_l2']:.6e} |"
        )
    lines += [
        "",
        f"- All physical-domain histories finite: `{summary['all_finite']}`",
        f"- Base timestep-halving maximum scalar difference: `{summary['timestep_max_relative_difference']:.6e}`",
        f"- Base MZ 8-to-16 maximum scalar difference: `{summary['toroidal_max_relative_difference']:.6e}`",
        "",
        "The proportional mesh cases quantify solution sensitivity but are not required",
        "to agree tightly because each Hypnotoad mesh changes the discrete machine",
        "geometry and metric representation. The timestep and toroidal checks isolate",
        "numerical sensitivities on the same base mesh.",
        "",
        "## Claim boundary",
        "",
        "This is short-duration linear evolution convergence with the serial topology",
        "override and without Jpar0. It is not exact-X-point evolution, validated ELM",
        "growth, nonlinear saturation, TCT control validation, or experimental agreement.",
        "",
    ]
    (run_dir / "diiid_bout_elm_solution_convergence_report.md").write_text(
        "\n".join(lines), encoding="utf-8"
    )


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--executable", type=Path, default=DEFAULT_EXE)
    parser.add_argument("--python", type=Path, default=Path("/root/.venvs/hypnotoad/bin/python"))
    parser.add_argument("--run-dir", type=Path, default=DEFAULT_RUN_DIR)
    args = parser.parse_args()
    if args.run_dir.exists():
        shutil.rmtree(args.run_dir)
    args.run_dir.mkdir(parents=True)

    with tempfile.TemporaryDirectory(prefix="diiid_elm_convergence_") as temp:
        mesh_dir = Path(temp)
        subprocess.run(
            [str(args.python), str(MESH_GENERATOR), "--run-dir", str(mesh_dir), "--keep-meshes"],
            check=True,
            capture_output=True,
            text=True,
        )
        serial_grids = {}
        for mesh_name in ("coarse", "base", "fine"):
            grid = mesh_dir / f"{mesh_name}_serial.grd.nc"
            write_serial_topology_grid(mesh_dir / f"{mesh_name}.grd.nc", grid)
            serial_grids[mesh_name] = grid
        cases = {}
        for case_name, mesh_name, mz, dt in CASES:
            case_dir = args.run_dir / case_name
            write_input(case_dir, serial_grids[mesh_name], mz, dt)
            run_case(args.executable, case_dir)
            cases[case_name] = summarize(case_dir, mesh_name, mz, dt)

    def comparison(first: str, second: str) -> dict[str, float]:
        return {
            field: relative_difference(
                cases[first]["fields"][field]["final_l2"], cases[second]["fields"][field]["final_l2"]
            )
            for field in FIELDS
        }

    timestep = comparison("base", "base_dt_half")
    toroidal = comparison("base", "base_mz16")
    all_finite = all(field["finite"] for case in cases.values() for field in case["fields"].values())
    timestep_max = max(timestep.values())
    toroidal_max = max(toroidal.values())
    passed = all_finite and timestep_max < 5e-3 and toroidal_max < 5e-3
    summary = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "status": "DIIID_ELM_PB_SERIAL_SOLUTION_CONVERGENCE_SUPPORTED" if passed else "DIIID_ELM_PB_SERIAL_SOLUTION_CONVERGENCE_FAILED",
        "cases": cases,
        "timestep_relative_differences": timestep,
        "toroidal_relative_differences": toroidal,
        "timestep_max_relative_difference": timestep_max,
        "toroidal_max_relative_difference": toroidal_max,
        "all_finite": all_finite,
    }
    (args.run_dir / "diiid_bout_elm_solution_convergence_summary.json").write_text(
        json.dumps(summary, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )
    write_report(args.run_dir, summary)
    print(json.dumps({"status": summary["status"], "timestep_max": timestep_max, "toroidal_max": toroidal_max}, indent=2))
    return 0 if passed else 1


if __name__ == "__main__":
    raise SystemExit(main())
