#!/usr/bin/env python3
"""Run serial-topology DIII-D elm-pb solution-convergence checks."""

from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import tempfile
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import netCDF4
import numpy as np

from diiid_bout_elm_mesh_smoke import write_serial_topology_grid


REPO = Path(__file__).resolve().parent
DEFAULT_EXE = Path("/tmp/bout-build/examples/elm-pb/elm_pb")
DEFAULT_RUN_DIR = REPO / "validation_runs" / "diiid_bout_elm_solution_convergence_default"
MESH_GENERATOR = REPO / "diiid_mesh_convergence_consistency.py"
FIELDS = ("U", "P", "Psi")
CASES = (
    ("coarse", "coarse", 8, 1e-4),
    ("base", "base", 8, 1e-4),
    ("fine", "fine", 8, 1e-4),
    ("base_dt_half", "base", 8, 5e-5),
    ("base_mz16", "base", 16, 1e-4),
)


